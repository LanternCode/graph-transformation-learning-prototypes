import os
import pickle
import subprocess
import torch
import pandas as pd
from transformers import BertTokenizer, BertModel
from torch_geometric.data import Data
from torch_geometric.nn import SAGEConv
from sklearn.metrics import classification_report, roc_auc_score
from torch.utils.data import DataLoader, TensorDataset


# A helper that takes a string (e.g. "apple"), tokenizes it, runs it through BERT in eval mode (no gradients),
# and average‑pools over all token embeddings to get a single 768‑dim vector. Average pooling collapses
# variable‑length token sequences into one vector
@torch.no_grad()
def encode_bert(text):
    inputs = tokenizer(text, return_tensors="pt", truncation=True, padding=True, max_length=32)
    outputs = bert_model(**inputs)
    return outputs.last_hidden_state.mean(dim=1).squeeze(0)  # shape: [768]


# To turn (head_str, tail_str) pairs into (head_idx, tail_idx) pairs that the GNN can work with,
# add numeric columns h_id and t_id by mapping through node2id.
def encode(df):
    return df.assign(
        h_id=df['h'].astype(str).map(node2id),
        t_id=df['t'].astype(str).map(node2id)
    )


# Define the GraphSAGE model
class EdgeClassifierGNN(torch.nn.Module):
    def __init__(self, in_channels, hidden_channels):
        super().__init__()
        self.sage1 = SAGEConv(in_channels, hidden_channels)
        self.sage2 = SAGEConv(hidden_channels, hidden_channels)
        self.classifier = torch.nn.Sequential(
            torch.nn.Linear(hidden_channels * 2, hidden_channels),
            torch.nn.ReLU(),
            torch.nn.Linear(hidden_channels, 1)
        )

    def forward(self, x, edge_index, edge_label_index):
        x = self.sage1(x, edge_index).relu()
        x = self.sage2(x, edge_index)
        h_src = x[edge_label_index[0]]
        h_dst = x[edge_label_index[1]]
        h_pair = torch.cat([h_src, h_dst], dim=1)
        return self.classifier(h_pair).squeeze()


