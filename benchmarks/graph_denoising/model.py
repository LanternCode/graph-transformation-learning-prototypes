if __name__ == "__main__":
    # Part 1: Download the dataset and transform the data for training
    # --- Clone the dataset using subprocess ---
    repo_dir = 'GOLD'
    if not os.path.isdir(repo_dir):
        print(f"Cloning GOLD repo into ./{repo_dir} …")
        subprocess.run(
            ["git", "clone", "https://github.com/HKUST-KnowComp/GOLD.git"],
            check=True
        )
    else:
        print(f"Repo already cloned at ./{repo_dir}, skipping clone.")

    # === CONFIGURATION ===
    base = 'GOLD/dataset/conceptnet'
    rules_dir = os.path.join(base, 'rules')
    noise_level = 'C-20'  # or C-5, C-10, C-20
    top_k = 500  # use top-K rules

    # === 1. Load clean data
    df_train = pd.read_csv(os.path.join(base, 'train.txt'), sep='\t', header=None, names=['h', 'r', 't'])
    df_valid = pd.read_csv(os.path.join(base, 'valid.txt'), sep='\t', header=None, names=['h', 'r', 't'])
    df_test = pd.read_csv(os.path.join(base, 'test.txt'), sep='\t', header=None, names=['h', 'r', 't'])

    df_train['label'] = 1
    df_valid['label'] = 1
    df_test['label'] = 1

    # === 2. Load a Python dict where each key is a relation name (e.g. “IsA”)
    # and each value is a list of the top‑K noisy candidate negatives generated by some rule (e.g. corrupting the tail)
    pkl_path = os.path.join(rules_dir, f'{noise_level}-rules-top-{top_k}.pkl')
    with open(pkl_path, 'rb') as f:
        neg_data = pickle.load(f)

    # === 2.1 To turn the nested rule‑based structure into a flat table of negative examples that parallel the positives
    # split each rule entry into (h, r, t) tuple and collect them all into one big list, then build a DataFrame and mark
    # them with label = 0. Each rule entry contains a parsed triple in string form (head rel tail).
    all_negatives = []
    for rel, entries in neg_data.items():
        for entry in entries:
            h, r, t = entry[1].split()  # parsed triple
            all_negatives.append((h, r, t))
    df_all_neg = pd.DataFrame(all_negatives, columns=['h', 'r', 't'])
    df_all_neg['label'] = 0

    # === 3. Slice the negatives DataFrame into three chunks: 80% for training, 10% for validation and 10% for testing,
    # based on the DataFrame’s row order.
    n_total_neg = len(df_all_neg)
    neg_train_end = int(0.8 * n_total_neg)
    neg_valid_end = int(0.9 * n_total_neg)

    df_train_neg = df_all_neg.iloc[:neg_train_end].copy()
    df_valid_neg = df_all_neg.iloc[neg_train_end:neg_valid_end].copy()
    df_test_neg = df_all_neg.iloc[neg_valid_end:].copy()

    # === 4. Randomly sample from the positive training triples to have exactly as many positives as negatives,
    # then concatenate them.
    df_train_pos_sampled = df_train.sample(n=len(df_train_neg), random_state=42)
    df_train_all = pd.concat([df_train_pos_sampled, df_train_neg], ignore_index=True)

    # === 5. Combine clean + noisy for validation and test
    df_valid_all = pd.concat([df_valid, df_valid_neg], ignore_index=True)
    df_test_all = pd.concat([df_test, df_test_neg], ignore_index=True)

    # Part 2: PLM
    # Load BERT-Base (12 transformer layers, hidden size 768, 12 attention heads)
    tokenizer = BertTokenizer.from_pretrained("bert-base-uncased")
    bert_model = BertModel.from_pretrained("bert-base-uncased")
    bert_model.eval()

    # GraphSAGE needs a contiguous node ID space (0...N–1) and a feature vector for nodes that ever appears in any split
    # Pull out the head and tail columns from all three splits, stack them into one Series of strings and deduplicate
    all_nodes = pd.concat([
        df_train_all[['h', 't']],
        df_valid_all[['h', 't']],
        df_test_all[['h', 't']]
    ]).stack().astype(str).unique()

    df_train_encoded = encode(df_train_all)
    df_valid_encoded = encode(df_valid_all)
    df_test_encoded = encode(df_test_all)

    # GraphSAGE conv layers treat the graph as undirected, so both directions must be present.
    # Having both (i→j) and (j→i) ensures that when computing a node’s new embedding, it aggregates
    # neighbors regardless of original edge orientation. Take the training edges' pairs, transpose
    # to shape [2, num_edges], make that a tensor and then concatenate the reversed edges for undirected message-passing
    edge_index = torch.cat([
        torch.tensor(df_train_encoded[['h_id', 't_id']].values.T, dtype=torch.long),
        torch.tensor(df_train_encoded[['t_id', 'h_id']].values.T, dtype=torch.long)
    ], dim=1)

    # For each unique node string, run encode_bert once and store its 768‑dim tensor.
    node2vec = {node: encode_bert(node) for node in all_nodes}

    # Sort the node strings (to fix an ordering) and assign each a unique integer ID.
    node2id = {node: idx for idx, node in enumerate(sorted(node2vec.keys()))}

    # Stack the embeddings in ID order into a single [N × 768] matrix (x[i] is the embedding for node with ID i).
    x = torch.stack([node2vec[node] for node in sorted(node2id)])

    # Final graph object
    graph = Data(x=x, edge_index=edge_index)

    # Part 3: GraphSAGE Training
    # Prepare training data
    edge_label_index = torch.tensor(df_train_encoded[['h_id', 't_id']].values.T, dtype=torch.long)
    edge_label = torch.tensor(df_train_encoded['label'].values, dtype=torch.float)
    edge_dataset = TensorDataset(edge_label_index[0], edge_label_index[1], edge_label)
    train_loader = DataLoader(edge_dataset, batch_size=1024, shuffle=True)

    # Training loop
    model = EdgeClassifierGNN(in_channels=768, hidden_channels=128)
    optimizer = torch.optim.Adam(model.parameters(), lr=0.001)
    criterion = torch.nn.BCEWithLogitsLoss()

    best_auc = 0.0
    for epoch in range(1, 21):
        model.train()
        total_loss = 0
        for h, t, y in train_loader:
            optimizer.zero_grad()
            edge_label_index = torch.stack([h, t])
            out = model(graph.x, graph.edge_index, edge_label_index)
            loss = criterion(out, y)
            loss.backward()
            optimizer.step()
            total_loss += loss.item()

        val_auc = evaluate(model, df_valid_encoded)
        print(f"Epoch {epoch:02d} - Loss: {total_loss:.4f} - Val AUC: {val_auc:.4f}")
        if val_auc > best_auc:
            best_auc = val_auc
            torch.save(model.state_dict(), 'bert_sage_best_model.pth')
            print(f"Saved model at epoch {epoch} with AUC {val_auc:.4f}")
